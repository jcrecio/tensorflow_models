feature_columns = []

for feature_name in ["ExternalView", "Floor", "Attic", "Elevator", "Neighbourhood"]:
  vocabulary = dftrain[feature_name].unique()
  feature_columns.append(tf.feature_column.categorical_column_with_vocabulary_list(feature_name, vocabulary))

for feature_name in ["Rooms", "Size", "Bathrooms"]:
	feature_columns.append(tf.feature_column.numeric_column(feature_name, dtype=tf.int32))
 
print(feature_columns)

feature_columns

"""Creates batches with the data and train for N epochs (we can shuffle or not)"""

def make_input_fn(data_df, label_df, num_epochs=10, shuffle=True, batch_size=32):
  def input_function():
    ds = tf.data.Dataset.from_tensor_slices((dict(data_df), label_df))
    if shuffle:
      ds = ds.shuffle(1000)
    ds = ds.batch(batch_size).repeat(num_epochs)
    return ds
  return input_function

train_input_fn = make_input_fn(dftrain, y_train, num_epochs=1, shuffle=True, batch_size=1)
eval_input_fn = make_input_fn(dfeval, y_eval, num_epochs=1, shuffle=False)

"""Now we create our model (Linear regression)"""

optimizer = tf.keras.optimizers.SGD(learning_rate=0.01)
linear_est = tf.estimator.LinearRegressor(feature_columns=feature_columns)
                                          
